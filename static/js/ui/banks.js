            row.addEventListener('click', (e) => {
                if (e.target.closest('.entry-btn')) return;
                document.querySelectorAll('#code-bank-list .bank-entry').forEach(r => r.classList.remove('selected'));
                row.classList.add('selected');
            });

            // Double-click name → load into editor (no confirm dialog)
