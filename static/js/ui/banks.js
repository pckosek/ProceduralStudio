// ── Code Bank ─────────────────────────────────────────────────
async function triggerCodeBankSave() {
    const defaultName = `snippet_${new Date().toISOString().slice(0,10)}`;
    const name = await openSaveNameModal(defaultName);
    if (name === null) return;

    const cleanName = name.trim() || 'Python Snippet';
    const key       = `code_${Date.now()}`;
    const payload   = { name: cleanName, code: editorInstance.getValue() };
    try {
        await dbSetCode(key, payload);
        await refreshCodeBankUI();
        printToConsole(`System: Saved code to Code Bank as "${cleanName}".\n`, 'out-system');
    } catch {
        printToConsole('System Error: Unable to save to Code Bank.\n', 'out-stderr');
    }
}

