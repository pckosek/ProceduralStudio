// ── Activity bar / sidebar wiring ────────────────────────────
let activeSidebarPanel = null; // 'explorer' | 'image-bank' | null

function setupActivityBar() {
    const sidebar      = document.getElementById('sidebar');
    const sidebarTitle = document.getElementById('sidebar-title');
    const closeBtn     = document.getElementById('sidebar-close-btn');
    const addBtn       = document.getElementById('sidebar-add-btn');

    document.querySelectorAll('.activity-btn[data-panel]').forEach(btn => {
        btn.addEventListener('click', () => {
            const panelId = btn.dataset.panel;

            if (activeSidebarPanel === panelId) {
                closeSidebar();
                return;
            }

            activeSidebarPanel = panelId;
            document.querySelectorAll('.activity-btn').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            document.querySelectorAll('.sidebar-body').forEach(p => p.classList.remove('active'));
            document.getElementById('panel-' + panelId).classList.add('active');

            const titles = { 'explorer': 'Project', 'image-bank': 'Image Bank' };
            sidebarTitle.textContent = titles[panelId] || panelId;

            if (panelId === 'explorer')   refreshExplorerUI();
            if (panelId === 'image-bank') refreshImageBankUI();

            sidebar.classList.remove('collapsed');
        });
    });

    closeBtn.addEventListener('click', closeSidebar);

    // + button: context-sensitive per active panel
    addBtn.addEventListener('click', () => {
        if (activeSidebarPanel === 'explorer')   promptNewFile('');
        if (activeSidebarPanel === 'image-bank') triggerImageBankSave();
    });
}

function closeSidebar() {
    const sidebar = document.getElementById('sidebar');
    sidebar.classList.add('collapsed');
    document.querySelectorAll('.activity-btn').forEach(b => b.classList.remove('active'));
    activeSidebarPanel = null;
}

// ── Save-name modal (replaces browser prompt) ─────────────────
// Returns a Promise that resolves to the entered name, or null on cancel.
function openSaveNameModal(defaultName = '') {
    return new Promise((resolve) => {
        const modal  = document.getElementById('save-name-modal');
        const input  = document.getElementById('save-name-input');
        const okBtn  = document.getElementById('save-name-ok');
        const cancel = document.getElementById('save-name-cancel');
        const close  = document.getElementById('save-name-close');

        input.value = defaultName;
        modal.classList.add('open');
        setTimeout(() => { input.focus(); input.select(); }, 60);

        function finish(value) {
            modal.classList.remove('open');
            okBtn.removeEventListener('click', onOk);
            cancel.removeEventListener('click', onCancel);
            close.removeEventListener('click', onCancel);
            input.removeEventListener('keydown', onKey);
            resolve(value);
        }
        function onOk()    { finish(input.value.trim() || defaultName || 'Untitled'); }
        function onCancel(){ finish(null); }
        function onKey(e)  {
            if (e.key === 'Enter')  onOk();
            if (e.key === 'Escape') onCancel();
        }
        okBtn.addEventListener('click', onOk);
        cancel.addEventListener('click', onCancel);
        close.addEventListener('click', onCancel);
        input.addEventListener('keydown', onKey);
    });
}

// ── Naming helpers ────────────────────────────────────────────
function getAutogeneratedName() {
    const now = new Date();
    const pad = n => String(n).padStart(2, '0');
    return `${now.getFullYear()}_${pad(now.getMonth()+1)}_${pad(now.getDate())}_${pad(now.getHours())}${pad(now.getMinutes())}_${canvas.width}x${canvas.height}`;
}

async function getUniqueAutogeneratedName() {
    const base    = getAutogeneratedName();
    const entries = await dbGetAllSavedImages();
    const existing = entries.map(e => e.data.name);
    let name = base, n = 1;
    while (existing.includes(name)) name = `${base}_${String(n++).padStart(2,'0')}`;
    return name;
}

// ── Image Bank ────────────────────────────────────────────────
async function triggerImageBankSave() {
    // Suggest current name as default (if canvas has one), so the user can
    // confirm overwrite by pressing Enter, or change it for Save-As semantics.
    // The name shown in the dialog is always visible — no silent overwrites.
    const currentName   = (typeof activeImageName !== 'undefined' && activeImageName) || null;
    const suggestedName = currentName || (await getUniqueAutogeneratedName());
    const name = await openSaveNameModal(suggestedName);
    if (name === null) return;  // cancelled

    try {
        const entries     = await dbGetAllSavedImages();
        const existingIdx = entries.findIndex(e => e.data.name === name);

        if (existingIdx !== -1) {
            // Overwrite: user confirmed the name matches an existing entry
            const existing  = entries[existingIdx];
            existing.data.base64 = canvas.toDataURL('image/png');
            await dbSet(existing.id, existing.data);
            printToConsole(`System: Saved (overwrite) "${name}" in Image Bank.\n`, 'out-system');
        } else {
            // New entry
            const key     = `bank_${Date.now()}`;
            const payload = { name, base64: canvas.toDataURL('image/png') };
            await dbSet(key, payload);
            printToConsole(`System: Saved canvas to Image Bank as "${name}".\n`, 'out-system');
        }

        // Update identity label and write session snapshot so refresh
        // restores this explicitly-saved state.
        setActiveImageName(name);
        await writeSessionSnapshot();
        await refreshImageBankUI();
    } catch {
        printToConsole('System Error: Unable to save to Image Bank.\n', 'out-stderr');
    }
}

async function refreshImageBankUI() {
    const list = document.getElementById('image-bank-list');
    list.innerHTML = '';

    try {
        const entries = await dbGetAllSavedImages();
        entries.sort((a, b) => b.timestamp - a.timestamp);

        if (entries.length === 0) {
            list.innerHTML = '<div class="bank-empty">No saved images.<br>Use + to save the active canvas.</div>';
            return;
        }

        entries.forEach(entry => {
            const time = new Date(entry.timestamp).toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' });
            const row  = document.createElement('div');
            row.className = 'bank-entry';
            row.dataset.id = entry.id;
            row.innerHTML = `
                <span class="bank-entry-icon">
                    <svg width="13" height="13" viewBox="0 0 16 16" fill="currentColor">
                        <path d="M6.002 5.5a1.5 1.5 0 1 1-3 0 1.5 1.5 0 0 1 3 0z"/>
                        <path d="M1.5 2A1.5 1.5 0 0 0 0 3.5v9A1.5 1.5 0 0 0 1.5 14h13a1.5 1.5 0 0 0 1.5-1.5v-9A1.5 1.5 0 0 0 14.5 2h-13zm13 1a.5.5 0 0 1 .5.5v6l-3.775-1.947a.5.5 0 0 0-.577.093l-3.71 3.71-2.66-1.772a.5.5 0 0 0-.63.062L1.002 12v.54A.505.505 0 0 1 1 12.5v-9a.5.5 0 0 1 .5-.5h13z"/>
                    </svg>
                </span>
                <span class="bank-entry-name" title="${entry.data.name}">${entry.data.name}</span>
                <span class="bank-entry-time">${time}</span>
                <span class="bank-entry-actions">
                    <button class="entry-btn load"   title="Load into canvas">↓</button>
                    <button class="entry-btn export" title="Download PNG">⬇</button>
                    <button class="entry-btn danger" title="Delete">✕</button>
                </span>
            `;

            // Selection
            row.addEventListener('click', (e) => {
                if (e.target.closest('.entry-btn')) return;
                document.querySelectorAll('#image-bank-list .bank-entry').forEach(r => r.classList.remove('selected'));
                row.classList.add('selected');
            });

            // Inline rename on name double-click
            row.querySelector('.bank-entry-name').addEventListener('dblclick', (e) => {
                e.stopPropagation();
                startInlineRename(row, entry, async (newName) => {
                    entry.data.name = newName;
                    await dbSet(entry.id, entry.data);
                    printToConsole(`System: Renamed image to "${newName}".\n`, 'out-system');
                    await refreshImageBankUI();
                });
            });

            // Load
            row.querySelector('.entry-btn.load').addEventListener('click', async () => {
                setCanvasFromBase64(entry.data.base64);
                setActiveImageName(entry.data.name);
                // Write snapshot so refresh restores this loaded image
                await writeSessionSnapshot();
            });

            // Download
            row.querySelector('.entry-btn.export').addEventListener('click', () => {
                const a = document.createElement('a');
                a.download = entry.data.name + '.png';
                a.href     = entry.data.base64;
                document.body.appendChild(a);
                a.click();
                document.body.removeChild(a);
                printToConsole(`System: Exported "${entry.data.name}.png".\n`, 'out-system');
            });

            // Delete
            row.querySelector('.entry-btn.danger').addEventListener('click', async () => {
                await dbDelete(entry.id);
                // If deleting the currently active named image, clear the identity
                if (typeof activeImageName !== 'undefined' && activeImageName === entry.data.name) {
                    setActiveImageName(null);
                }
                await refreshImageBankUI();
                printToConsole(`System: Removed "${entry.data.name}" from Image Bank.\n`, 'out-system');
            });

            list.appendChild(row);
        });
    } catch (err) {
        console.warn('Image Bank UI refresh failed', err);
    }
}

