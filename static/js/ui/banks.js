// ── Inline rename helper (shared by both banks) ───────────────
function startInlineRename(row, entry, onSave) {
    const nameEl = row.querySelector('.bank-entry-name');
    const originalName = nameEl.textContent;
    const input = document.createElement('input');
    input.type = 'text';
    input.className = 'bank-entry-name-input';
    input.value = originalName;
    nameEl.replaceWith(input);
    input.focus();
    input.select();

    let saved = false;
    const save = async () => {
        if (saved) return;
        saved = true;
        const newName = input.value.trim();
        if (newName && newName !== originalName) {
            await onSave(newName);
        } else {
            // No change — just restore text node
            const span = document.createElement('span');
            span.className = 'bank-entry-name';
            span.title = originalName;
            span.textContent = originalName;
            input.replaceWith(span);
        }
    };
    input.addEventListener('blur', save);
    input.addEventListener('keydown', (e) => {
        if (e.key === 'Enter')  save();
        if (e.key === 'Escape') {
            saved = true;
            const span = document.createElement('span');
            span.className = 'bank-entry-name';
            span.title = originalName;
            span.textContent = originalName;
            input.replaceWith(span);
        }
    });
}

